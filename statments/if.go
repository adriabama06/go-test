package main

func main() {
	if true {
		println("true")
	} else {
		println("false")
	}

	var x int = 5
	var y int = 10
	if x < y {
		println("x is less than y")
	}
	if x == y {
		println("x is equal to y")
	}
	if x > y {
		println("x is greater than y")
	}
	if x != y {
		println("x is not equal to y")
	}

}
